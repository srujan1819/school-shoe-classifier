import pandas as pd

def plot_per_class_accuracy(model, generator):
    generator.reset()
    y_true = generator.classes
    y_pred = np.argmax(model.predict(generator), axis=1)

    # Calculate accuracy for each class
    cm = confusion_matrix(y_true, y_pred)
    class_accuracies = cm.diagonal() / cm.sum(axis=1)

    # Create Bar Chart
    plt.figure(figsize=(12, 6))
    colors = plt.cm.viridis(np.linspace(0, 1, len(class_names)))
    plt.bar(class_names, class_accuracies, color=colors)

    plt.title('MobileNetV2: Accuracy by Category')
    plt.ylabel('Accuracy Score (0 to 1.0)')
    plt.xticks(rotation=45)
    plt.ylim(0, 1.1)

    # Add labels on top of bars
    for i, v in enumerate(class_accuracies):
        plt.text(i, v + 0.02, f"{v:.2f}", ha='center', fontweight='bold')

    plt.show()

