def plot_mobilenet_error_analysis(model, generator):
    generator.reset()
    y_true = generator.classes
    y_pred_probs = model.predict(generator)
    y_pred = np.argmax(y_pred_probs, axis=1)
    errors = np.abs(y_true - y_pred_probs.max(axis=1))

    fig, axes = plt.subplots(2, 2, figsize=(16, 12))
    fig.suptitle('MobileNetV2 - Comprehensive Error Analysis', fontsize=16, fontweight='bold')

    # 1. Error Distribution
    axes[0, 0].hist(errors, bins=25, color='#e74c3c', alpha=0.7, edgecolor='black')
    axes[0, 0].set_title('Error Distribution', fontweight='bold')
    axes[0, 0].set_xlabel('Prediction Error')

    # 2. Prediction vs True Label
    axes[0, 1].scatter(y_true, y_pred_probs.max(axis=1), alpha=0.4, color='#3498db')
    axes[0, 1].plot([0, len(class_names)-1], [0, 1], '--r', label='Perfect Prediction')
    axes[0, 1].set_title('Prediction vs True Label', fontweight='bold')

    # 3. Error Distribution by Class (Box Plot)
    sns.boxplot(x=y_true, y=errors, ax=axes[1, 0], palette='Blues')
    axes[1, 0].set_title('Error Distribution by Class', fontweight='bold')
    axes[1, 0].set_xticklabels(class_names, rotation=45)

    # 4. Confusion Matrix
    cm = confusion_matrix(y_true, y_pred)
    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', ax=axes[1, 1],
                xticklabels=class_names, yticklabels=class_names)
    axes[1, 1].set_title('Confusion Matrix', fontweight='bold')

    plt.tight_layout(rect=[0, 0.03, 1, 0.95])
    plt.show()

